package code.day02_variables;

public class c2_printVsPrintln { // opening bracket for class

    public static void main(String[] args){
        //first line on your consule
        System.out.println("This is println ");//println will break the current line

        //second line in your consule
        System.out.print("this is print ");//print will never break your line in the consule
        //second line in the consule
        System.out.print("continue to print");

        //second line
        System.out.print("continue to print 2 ");

        //second line
        System.out.println("println 2");//this will break the current line

        //will be on third line
        System.out.println("Println continue"); //this println will break the line

        //next thing will be on 4 line
        System.out.print("print continue ");

        //will be on 4 line

    }


}


